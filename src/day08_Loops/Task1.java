package day08_Loops;

public class Task1 {

	public static void main(String[] args) {
		//   for loop
		
		for(int a=20 ; a<=30 ; a++) {
			System.out.println(a);
			
					
		}

		
		// while loop
		int count =20; 
		while (count <=30) {
			System.out.println(count);
			count ++; 
		}
		
		//do loop
		 count =20; 
		 do {
			 System.out.println(count);
			 count ++; 
			 
		 }while (count<=30); 
		
	}
	

}
